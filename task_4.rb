def task_4(num)
  # Выводим числа от 1 до num
  puts "   #{(1..num).to_a.join('  ')}"
  # Перебираем этот диапазон чисел от 1 до num 
  # И сохраняем эти числа в масив row
  (1..num).each do |i| 
    row = [i]
    #  Перемножаем числа и добовляем их в этот же массив 
    (1..num).each do |j|
      row << i * j
    end
      # Добовляем пробелы в массив и выводим на экран
      puts row.join("  ")
  end

end